_INVERSE: dict[str, list[str]] = {}
for _tag, _codes in TAG_STOCKS.items():
    for _c in _codes:
        if _tag not in _INVERSE.get(_c, []):
            _INVERSE.setdefault(_c, []).append(_tag)


def get_tags(stock_code: str) -> list[str]:
    return _INVERSE.get(stock_code, [])


def has_ai_concept(stock_code: str) -> bool:
    return stock_code in _INVERSE


def ai_bonus(stock_code: str, weight: float = 5.0) -> float:
    return weight if has_ai_concept(stock_code) else 0.0


def all_ai_codes() -> set[str]:
    return set(_INVERSE.keys())


def tag_count() -> tuple[int, int]:
    """Returns (number of tags, number of unique stocks)."""
    return len(TAG_STOCKS), len(_INVERSE)
